        <h1>Sophie et le Relieur</h1>
        <p>Amoureux de son quartier, il fait visiter vers 2004, le vieux Paris à Hideko ISE, artiste peintre et illustratrice japonaise reconnue au Japon. Elle réalise pendant deux ans un album qui le met en scène avec sa petite-fille : "Sophie et le Relieur". Cet album savoureux raconte sous la forme d'aquarelles, la rencontre de deux générations : Sophie la petite-fille qui veut faire réparer son herbier et « le Relieur », vieil homme artiste qui va nous faire découvrir son métier de Relieur (typiquement français). Paru en 2006 au Japon « Sophie et le Relieur » gagne le prix littéraire Kodansha du livre illustré au Japon. La version francaise paraît en 2007 au Seuil. Mon père participe bénévolement à la rédaction du texte de l'édition française en essayant de corriger chez l'éditeur certaines erreurs en rapport avec les techniques de la Reliure. Une équipe de NHK se déplace à Paris pour le filmer avec Hideko ISE, et le présente au journal télévisé du matin sur NHK.</p>
        <p>Elu en 2001 conseiller municipal du 6eme arr. de Paris sur la liste de son ami Jean-Pierre Lecoq, mon père joue pendant sa mandature un rôle constructif, parfois corrosif au sein de la municipalité mais toujours dans le sens de l' intéret des habitants du Quartier Latin. Gravement malade dès 2009, il décède en 2014.</p>
