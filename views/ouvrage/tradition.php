        <h1>D'Athènes à Paris</h1>
        <p>
            Au XIXeme siècle la famille Minos comptait déjà plusieurs générations de relieurs à Athènes. Mon arrière grand-père Christos Minos fut le premier à importer un massicot en Grèce.
        </p>
        <p>
            Travaillant pour la Famille Royale de Grèce, cette tradition se poursuivit avec mon grand-père Nicolas Minos (1887-1967). En 1912-1913, Nicolas Minos fut incorporé dans la marine grecque et participa aux guerres gréco-turques. Il y fut blessé et rejoint la France en 1916. Incorporé dans les forces alliées il y termina la guerre jusqu'en 1918. Après la victoire il ouvrit son atelier de reliure à Paris au Quartier Latin (rue de la Harpe puis rue du Petit-Pont). En relation avec l'Ambassade de Grèce, il travailla principalement pour les princes Nicolas et Georges de Grèce, pour son amie la princesse Marina de Grèce (future duchesse de Kent), la princesse Eugénie, Marie Bonaparte et de nombreuses personnalités.
        </p>
        <p>
            Mes grands-parents prirent leur retraite en Bretagne d'où ma grand-mère était originaire.
        </p>
